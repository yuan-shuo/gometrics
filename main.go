// ========== 主函数 ==========

func main() {
	// 读取 YAML 文件
	data, err := os.ReadFile("internal/metrics/definition.yaml")
	if err != nil {
		fmt.Fprintf(os.Stderr, "Error reading definition.yaml: %v\n", err)
		os.Exit(1)
	}

	var config MetricConfig
	if err := yaml.Unmarshal(data, &config); err != nil {
		fmt.Fprintf(os.Stderr, "Error parsing YAML: %v\n", err)
		os.Exit(1)
	}

	// 准备模板函数
	funcMap := template.FuncMap{
		"toPascal":    toPascal,
		"hasMethod":   hasMethod,
		"labelParams": labelParams,
		"labelArgs":   labelArgs,
		"joinLabels":  joinLabels,
	}

	tmpl, err := template.New("metrics").Funcs(funcMap).Parse(templateText)
	if err != nil {
		fmt.Fprintf(os.Stderr, "Error parsing template: %v\n", err)
		os.Exit(1)
	}

	// 执行模板
	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, config); err != nil {
		fmt.Fprintf(os.Stderr, "Error executing template: %v\n", err)
		os.Exit(1)
	}

	// 格式化代码
	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		fmt.Fprintf(os.Stderr, "Error formatting code: %v\n", err)
		fmt.Fprintf(os.Stderr, "Raw output:\n%s\n", buf.String())
		os.Exit(1)
	}

	// 写入文件
	outputPath := filepath.Join("internal", "metrics", "metrics_gen.go")
	if err := os.WriteFile(outputPath, formatted, 0644); err != nil {
		fmt.Fprintf(os.Stderr, "Error writing file: %v\n", err)
		os.Exit(1)
	}

	fmt.Println("Generated internal/metrics/metrics_gen.go")
}
