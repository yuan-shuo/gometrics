// ========== 数据结构 ==========

type MetricConfig struct {
	ServiceName string      `yaml:"service_name"`
	Subsystems  []Subsystem `yaml:"subsystems"`
}

type Subsystem struct {
	Name       string      `yaml:"name"`
	Counters   []Metric    `yaml:"counters"`
	Gauges     []Metric    `yaml:"gauges"`
	Histograms []Histogram `yaml:"histograms"`
}

type Metric struct {
	Name    string   `yaml:"name"`
	Help    string   `yaml:"help"`
	Labels  []string `yaml:"labels"`
	Methods []string `yaml:"methods"`
}

type Histogram struct {
	Metric
	Buckets []float64 `yaml:"buckets"`
}

// ========== 辅助函数 ==========

func toPascal(s string) string {
	parts := strings.Split(s, "_")
	for i, part := range parts {
		if len(part) > 0 {
			parts[i] = strings.ToUpper(part[:1]) + part[1:]
		}
	}
	return strings.Join(parts, "")
}

func hasMethod(methods []string, target string) bool {
	for _, m := range methods {
		if m == target {
			return true
		}
	}
	return false
}

func joinLabels(labels []string) string {
	if len(labels) == 0 {
		return ""
	}
	return strings.Join(labels, ", ")
}

func labelParams(labels []string) string {
	if len(labels) == 0 {
		return ""
	}
	params := make([]string, len(labels))
	for i, label := range labels {
		params[i] = label + " string"
	}
	return strings.Join(params, ", ")
}

func labelArgs(labels []string) string {
	if len(labels) == 0 {
		return ""
	}
	return strings.Join(labels, ", ")
}

